import java.util.Scanner;

    public class Repetindo {
        public static void main(String[] args){
            Scanner in = new Scanner(System.in);
            int numero;

        // FAZ ISSO
        do {
            System.out.println("Informe um numero:::::");
            numero = in.nextInt();
        
        // ENQUANTO ( NUMERO FOR MAIOR OU IGUAL A 0)
        }    
        while (numero >= 0);
        // LOGO SE FOR MENOR QUE ZERO ELE EXECUTA ISSO...
        System.out.println("Numero fora de comando");
     }
    }
